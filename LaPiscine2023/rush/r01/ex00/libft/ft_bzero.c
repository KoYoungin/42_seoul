#include "libft.h"

void	*ft_bzero(void *b, size_t len)
{
	char	*p;
	size_t	i;

	i = 0;
	if (len)
	{
		p = b;
		while (i < len)
		{
			p[i] = 0;
			i++;
		}
	}
	return (b);
}
